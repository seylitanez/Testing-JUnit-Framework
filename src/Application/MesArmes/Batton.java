package Application.MesArmes;

public class Batton {
	private float atq=10,def=50;
	public Batton(float atq,float def) {
		this.atq=atq;
		this.def=def;
	}
	public float getAtq() {
		return atq;
	}
	public void setAtq(float atq) {
		this.atq = atq;
	}
	public float getDef() {
		return def;
	}
	public void setDef(float def) {
		this.def = def;
	}
	
	

}
